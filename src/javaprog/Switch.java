package javaprog;

public class Switch {
	public static void main(String args[]) {
		int number=3;
		switch(number) {
		case 0:
			System.out.println("number is 0");
			break;
		
		case 2:
			System.out.println("number is2");
			break;
		case 3:
			System.out.println("number is 3");
			break;
			default:
				System.out.println("no cases selected");
	}
	
	}
}
